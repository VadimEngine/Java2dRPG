package other;

public class SolidBlock {
	
	private int x;
	private int y;
	private int width = 32;
	private int height = 32;
	
	public SolidBlock(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int getWidth() {
		return width;
	}
	
	public int getHeight() {
		return height;      
	}

	
}
